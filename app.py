# 3. Função de Emissão
def emitir_certificado(endereco_aluno, url_json_gist):
    print(f"Iniciando emissão para: {endereco_aluno}...")

    nonce = w3.eth.get_transaction_count(public_key_universidade)

    tx = contract.functions.issueDiploma(
        endereco_aluno,
        url_json_gist
    ).build_transaction({
        'chainId': 1337,  # ID do Ganache (ou 11155111 para Sepolia)
        'gas': 2000000,
        'gasPrice': w3.to_wei('20', 'gwei'),
        'nonce': nonce
    })

    signed_tx = w3.eth.account.sign_transaction(
        tx, private_key_universidade)

    tx_hash = w3.eth.send_raw_transaction(signed_tx.raw_transaction)
    print(f"Transação enviada! Hash: {w3.to_hex(tx_hash)}")

    receipt = w3.eth.wait_for_transaction_receipt(tx_hash)
    print(f"Confirmado no bloco: {receipt.blockNumber}")
    print("Certificado emitido com sucesso!")


